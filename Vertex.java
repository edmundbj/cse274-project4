
public class Vertex {
	
	// ========================================================== Properties
	
	private String symbol, address;
	
	// ========================================================== Constructors
	
	public Vertex(String symbol, String address) {
		this.symbol = symbol;
		this.address = address;
	}
	
	// ========================================================== Methods
	
	public String getSymbol() {
		return symbol;
	}
	
	public String getAddress() {
		return address;
	}
	
}
